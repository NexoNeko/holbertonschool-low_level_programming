#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - adds two integers
 *
 * @a: int a
 * @b: int b
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs b from a
 *
 * @a: int a
 * @b: int b
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a * b
 *
 * @a: int a
 * @b: int b
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a by b
 *
 * @a: int a
 * @b: int b
 * Return: a / b
 */
int op_div(int a, int b)
{
		return (a / b);
}
/**
 * op_mod - performs modulo b on a
 *
 * @a: int a
 * @b: int b
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
